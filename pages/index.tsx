import Head from "next/head";
import React from "react";
import Carousel from "../components/carousel";
import MediaCard from "../components/MediaCard";
import Unauthorized from "./Unauthorized";
import { useSession } from "next-auth/client";
import prisma from "../lib/prisma";

export const getServerSideProps = async (context: any) => {
  const allMoviesTEST = await prisma.movie.findMany({});
  const allSeriesTEST = await prisma.series.findMany({});
  console.log(allSeriesTEST);
  return { props: { allMoviesTEST, allSeriesTEST } };
};

export default function Home(props: any) {
  const [session, loading] = useSession();

  const [state, setState] = React.useState({
    moviesTest: [],
    seriesTest: [],
  });
  let maxMoviesTest = 10;

  React.useEffect(() => {
    setState({
      ...state,
      moviesTest: props.allMoviesTEST.slice(-5),
      seriesTest: props.allSeriesTEST.slice(-5),
    });
  }, []);

  if (session) {
    console.log(session);
    return (
      <div>
        <Head>
          <title>MediaTracker</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>

        <main className="bg-primary-dark h-full overflow-auto">
          <h1 className="text-white font-bold text-2xl m-4">
            Welcome, {session?.user?.name}
          </h1>

          <h2 className="ml-4 text-white font-semibold text-xl">
            Recently Watched...
          </h2>
          <div className="m-4 flex overflow-hidden">
            {state.moviesTest.map((item: any) => {
              let card = (
                <MediaCard
                  title={item.title}
                  poster={item.poster}
                  mediaType="movie"
                  id={item.id}
                  key={item.id}
                ></MediaCard>
              );

              return card;
            })}
          </div>
          <h2 className="ml-4 text-white mt-2 font-semibold text-xl">
            Your List...
          </h2>
          <div className="m-4 flex overflow-hidden">
            {state.seriesTest.map((item: any) => {
              let card = (
                <MediaCard
                  title={item.title}
                  poster={item.poster}
                  mediaType="series"
                  id={item.id}
                  key={item.id}
                ></MediaCard>
              );

              return card;
            })}
          </div>
        </main>
      </div>
    );
  }
  if (loading) {
    return <div className="m-4 text-white">Loading...</div>;
  }
  return <Unauthorized></Unauthorized>;
}
